public class ExampleIfElse {

    public static void main(String[] args) {
        int x , y;
        x = 20;
        y = 30;
        if (x > 0 && y >0) {
            System.out.println("Both numbers are positive");
        } else if (x>0 || y > 0) {
            System.out.println("At least one number is positive");
        } else {
            System.out.println(" Both numbers are negative");
        }
        }}





